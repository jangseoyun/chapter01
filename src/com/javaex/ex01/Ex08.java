package com.javaex.ex01;

public class Ex08 {
	public static void main(String[] args) {
		
		
		//관계 연산자 결과값은 boolean형
		int n1 = 5;
		int n2 = 3;
		
		System.out.println(n1<n2); //5는 3보다 작니? false
		System.out.println(n1>n2); //5는 3보다 큰가요? true
		System.out.println(n1<=n2); //5는 3보다 같거나 작니? 
		System.out.println(n1>=n2); //5는 3보다 크거나 같니?
		System.out.println(n1==n2); //5와 3이 같니?
		System.out.println(n1!=n2); //5와 3은 다르니?
		
		
	}

}
